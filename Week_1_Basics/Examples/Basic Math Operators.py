a = 2
b = 3

#addition

print("Addition:", a + b)

#Subtraction

print("Subtraction:", a - b)

#Division

print("Division:", a / b)

#Multiplication

print("Multiplication:", a * b)

#Floor Division is a division operation that truncates the result to the nearest whole number towards infinity

print("Floor Division:", b // a)

#Modulus Operators give the remainder of the result of division between two values

print("Modulus:", b % a)
#Exponentiation

print("Exponentiation", a ** b)

